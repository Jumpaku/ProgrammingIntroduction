package jumpaku.programming.operations

fun main(args: Array<String>) {
    println("${10 == 8}") // false
    println("${"abc" != "xyz"}") // true
    println("${28.9 < 30.0}") // true
    println("${"abc" <= "xyz"}") // true
    println("${28.9 > 30.0}") // false
    println("${"abc" >= "xyz"}") // false
}
