package jumpaku.programming.variables

fun main(args: Array<String>) {

    // 変数定義
    val integer = 123 // Int（整数）
    val real = 123.0 // Double（実数）
    val string = "abc" // String（文字列）
    val truth = true // Boolean（真理値）

    // 変数の使用
    println("integer = $integer") // integer = 123
    println("real = $real") // real = 123.0
    println("string = $string") // string = abc
    println("truth = $truth") // truth = true
}